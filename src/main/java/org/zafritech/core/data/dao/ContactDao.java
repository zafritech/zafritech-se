package org.zafritech.core.data.dao;

/**
 *
 * @author LukeS
 */
public class ContactDao {

    private String userUuId;

    private String email;
    
    private String firstName;

    private String lastName;
    
    private String address;
    
    private String city;
    
    private String state;

    private String country;
    
    private String postCode;

    private String phone;
    
    private String mobile;

    private String website;

    public ContactDao() {
        
    }

    @Override
    public String toString() {
        
        return "ContactDao{" + "userUuId=" + userUuId + ", email=" + 
                email + ", firstName=" + firstName + ", lastName=" + 
                lastName + ", address=" + address + ", city=" + city + ", state=" + 
                state + ", country=" + country + ", postCode=" + 
                postCode + ", phone=" + phone + ", mobile=" + mobile + ", website=" + 
                website + '}';
    }

    public String getUserUuId() {
        return userUuId;
    }

    public void setUserUuId(String userUuId) {
        this.userUuId = userUuId;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getPostCode() {
        return postCode;
    }

    public void setPostCode(String postCode) {
        this.postCode = postCode;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public String getWebsite() {
        return website;
    }

    public void setWebsite(String website) {
        this.website = website;
    }
}
