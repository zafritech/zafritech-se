package org.zafritech.applications.integration.data.dao;

/**
 *
 * @author Luke Sibisi
 */
public class InterfaceCommentUpdateDao {
 
    private Long commentId;
    
    private String comment;
    
    private String action;
    
    private Long commentById;
    
    private String creationDate;
    
    private String status;
    
    private String actionBy;

    public InterfaceCommentUpdateDao() {
        
    }

    public Long getCommentId() {
        return commentId;
    }

    public void setCommentId(Long commentId) {
        this.commentId = commentId;
    }

    public String getComment() {
        return comment;
    }

    public void setComment(String comment) {
        this.comment = comment;
    }

    public String getAction() {
        return action;
    }

    public void setAction(String action) {
        this.action = action;
    }

    public Long getCommentById() {
        return commentById;
    }

    public void setCommentById(Long commentById) {
        this.commentById = commentById;
    }

    public String getCreationDate() {
        return creationDate;
    }

    public void setCreationDate(String creationDate) {
        this.creationDate = creationDate;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getActionBy() {
        return actionBy;
    }

    public void setActionBy(String actionBy) {
        this.actionBy = actionBy;
    }
}
